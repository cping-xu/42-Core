#include "pushswap.h"

void	rrb(char **str)
{
	char	*temp;
	int		i;

	i = 0;
	while (str[i])
	{
		i++;
	}
	i = i - 1;
	temp = str[i];
	while (i > 0)
	{
		str[i] = str[i - 1];
		i--;
	}
	str[0] = temp;
	write (1, "rrb\n", 4);
}
