#include "pushswap.h"

void	pa(char **a, char **b)
{
	char	*temp;
	int		i;

	i = 0;
	temp = b[0];
	if (!(a[i]))
		a[i] = NULL;
	while (a[i])
		i++;
	while (i > 0)
	{
		a[i] = a[i - 1];
		i--;
	}
	a[i] = temp;
	while (b[i + 1])
	{
		b[i] = b[i + 1];
		i++;
	}
	b[i] = NULL;
	write (1, "pa\n", 3);
}
