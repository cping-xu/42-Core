#include "pushswap.h"

void	ra(char **str)
{
	char	*temp;
	int		i;

	i = 0;
	temp = str[0];
	while (str[i + 1])
	{
		str[i] = str[i + 1];
		i++;
	}
	str[i] = temp;
	write (1, "ra\n", 3);
}
