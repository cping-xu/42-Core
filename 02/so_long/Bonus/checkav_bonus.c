#include "../include/so_long_bonus.h"

int	checkav(char **av)
{
	int	i;

	i = 0;
	while (av[1][i])
		i++;
	i = i - 1;
	if (av[1][i--] == 'r')
	{
		if (av[1][i--] == 'e')
		{
			if (av[1][i] == 'b')
				return (0);
		}
	}
	return (1);
}
