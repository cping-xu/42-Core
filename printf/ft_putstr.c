#include "ft_printf.h"

int	ft_putstr(char *s, int l)
{
	int	i;

	i = 0;
	if (!(s))
	{
		l = ft_putstr("(null)", l);
		return (l);
	}
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
		l++;
	}
	return (l);
}
